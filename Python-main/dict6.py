a = {'Mary': 20, 'Veronica': 30, 'Charles': 25, 'Hazel': 23}
print("Dictionary : ",a)
print("ascending order using keys : ",sorted(a.keys()))
print("ascending order using values : ",sorted(a.values()))
print("descending order using keys : ",sorted(a.keys(),reverse=True))
print("descending order using values : ",sorted(a.values(),reverse=True))
print("ascending order using keys and values : ",sorted(a.items()))
print("descending order using keys and values : ",sorted(a.items(),reverse=True))
